package com.cts.dnb.Application2;


public class ChannelStatus {
	private String chName;
	private String chStatus;
	private String host;
	private String subState;
	private String chType;
	private String lstMsgDt;
	private String lstMsgTm;
	
	
	public String getChName() {
		return chName;
	}
	public void setChName(String chName) {
		this.chName = chName;
	}
	public String getChStatus() {
		return chStatus;
	}
	public void setChStatus(String chStatus) {
		this.chStatus = chStatus;
	}
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public String getSubState() {
		return subState;
	}
	public void setSubState(String subState) {
		this.subState = subState;
	}
	public String getChType() {
		return chType;
	}
	public void setChType(String chType) {
		this.chType = chType;
	}
	public String getLstMsgDt() {
		return lstMsgDt;
	}
	public void setLstMsgDt(String lstMsgDt) {
		this.lstMsgDt = lstMsgDt;
	}
	public String getLstMsgTm() {
		return lstMsgTm;
	}
	public void setLstMsgTm(String lstMsgTm) {
		this.lstMsgTm = lstMsgTm;
	}
	
}
